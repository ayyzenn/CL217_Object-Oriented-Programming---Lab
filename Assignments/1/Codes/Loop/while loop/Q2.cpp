#include<iostream>
using namespace std;
int main()
{
    int i=0, j=0;
    cout<<"Even numbers"<<endl;
    while (i<=10)
    {
       if (i%2==0)
        {
            cout<<i<<endl;
        }
        i++;
        
    }
    cout<<endl;
    cout<<"Odd numbers"<<endl;
    while (j<=10)
    {
        if (j%2 != 0)
        {
            cout<<j<<endl;
        }
        j++;
    }
    
}






